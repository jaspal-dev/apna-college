public class AirthmeticUrinaryOperator {
    public static void main(String[] args) {
        int a = 10;
        // First Use then Increment
        System.out.println(a++);
        System.out.println(a);

        // Increment then use
        System.out.println(++a);
        System.out.println(a);

        // Use and decrement
        System.out.println(a--);
        System.out.println(a);

        // Decrement and use
        System.out.println(--a);
        System.out.println(a);
    }
}
